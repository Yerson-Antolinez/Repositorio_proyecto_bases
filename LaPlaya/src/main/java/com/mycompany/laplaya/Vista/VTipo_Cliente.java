    public void cargarModelo(){
        modelo.addColumn("ID_Tipo_Cliente");
        modelo.addColumn("Estado");
        modelo.addColumn("Descripcion");
        
    
        tblTipo_Cliente.setModel(modelo);
    }
     public void LimpiarCampos(){
           txtid_tipo_cliente.setText("");
           cmbEstado.setSelectedItem(null);
           txtdescripcion.setText("");
           
       } 
    
    
    
    
    public void cargarFormulario(){
        int filaselect = tblTipo_Cliente.getSelectedRow();
        txtid_tipo_cliente.setText(""+tblTipo_Cliente.getValueAt(filaselect, 0));
        cmbEstado.setSelectedItem((String) tblTipo_Cliente.getValueAt(filaselect, 1));
        txtdescripcion.setText((String)tblTipo_Cliente.getValueAt(filaselect, 2));
    }
    
    
    
    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
        int id_tipo_cliente = Integer.parseInt(txtid_tipo_cliente.getText());
        String estado = (String) cmbEstado.getSelectedItem();
        String descripcion = txtdescripcion.getText();
        
        Tipo_Cliente tipo_cliente = new Tipo_Cliente(id_tipo_cliente,estado,descripcion);
        ctc.eliminarTipoCliente(tipo_cliente);
        ctc.cargarTablaTipoCliente(tblTipo_Cliente);
        LimpiarCampos();
             
        
        // TODO add your handling code here:
    }//GEN-LAST:event_btneliminarActionPerformed

