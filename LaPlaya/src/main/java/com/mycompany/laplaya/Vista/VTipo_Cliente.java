    private void btnbuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbuscarActionPerformed
        // BOTON BUSCAR
        int id_tipo_cliente = Integer.parseInt(JOptionPane.showInputDialog(null, "Digite el numero de ID"));
        String estado = (String) cmbEstado.getSelectedItem();
        String descripcion = txtdescripcion.getText();
        
        
        Tipo_Cliente tipo_cliente = new Tipo_Cliente(id_tipo_cliente,estado,descripcion);
        ctc.buscarTipoCliente(tblTipo_Cliente, tipo_cliente);
        
               
        
    }//GEN-LAST:event_btnbuscarActionPerformed

