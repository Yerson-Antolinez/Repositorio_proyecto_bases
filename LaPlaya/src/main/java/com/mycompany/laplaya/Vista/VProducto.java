         int cantidad = Integer.parseInt(txtCantidad.getText());
         int tipo_producto =  cmbTipo.getSelectedIndex();
         int precio = Integer.parseInt(txtPrecio.getText());
         String fecha= form.format(jdtFecha.getDate());
         int ubicacion =  cmbUbicacion.getSelectedIndex();
         
        
