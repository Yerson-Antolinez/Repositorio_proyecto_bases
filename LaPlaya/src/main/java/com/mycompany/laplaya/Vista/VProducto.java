        public void cargarFormulario(){
        
        
        int filaselect = tblProducto.getSelectedRow();
        txtId.setText(""+tblProducto.getValueAt(filaselect, 0));
        txtNombre.setText((String)tblProducto.getValueAt(filaselect, 1));
        txtDescripcion.setText((String)tblProducto.getValueAt(filaselect, 2));
        txtCantidad.setText(""+tblProducto.getValueAt(filaselect, 3));
        cmbTipo.setSelectedItem((String) tblProducto.getValueAt(filaselect, 4));
        txtPrecio.setText(""+tblProducto.getValueAt(filaselect, 5));
        
        Date fechaC=null;
        try {
            fechaC = form.parse(tblProducto.getValueAt(filaselect, 6).toString());
        } catch (ParseException ex) {
           Logger.getLogger(VEmpleado.class.getName()).log(Level.SEVERE, null, ex);
        }
        jdtFecha.setDate(fechaC);
        cmbUbicacion.setSelectedItem((String) tblProducto.getValueAt(filaselect, 7));    
    }
