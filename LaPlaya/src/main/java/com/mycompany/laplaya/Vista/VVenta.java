    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
        
        
         
         int id_venta = Integer.parseInt(txtId.getText());
         int clienteid =  cmbVCliente.getSelectedIndex();
         int productoid = cmbProducto.getSelectedIndex();

         int precio = Integer.parseInt(txtPrecio.getText());
         int cantidad = (int) spCantidadPor.getValue();

        // Obtener la fila seleccionada
        int filaSeleccionada = tblVenta.getSelectedRow();

        // Verificar si hay una fila seleccionada
        if (filaSeleccionada != -1) {
            // Actualizar el valor en la tabla
            tblVenta.setValueAt(cantidad, filaSeleccionada, 3);
        } else {
            // Manejar el caso en que no haya una fila seleccionada
            JOptionPane.showMessageDialog(null, "Seleccione una fila para actualizar la cantidad.");
        }
         String fecha = form.format(jdtFecha.getDate());
         int empleadoid =  cmbVEmpleado.getSelectedIndex();
         int venta_total = precio * cantidad;
//         
//          int venta_total = Integer.parseInt(txtVentaTotal.getText());
//         int venta_total = precio * cantidad;
         
        Venta venta  = new Venta(id_venta, clienteid,productoid, cantidad, fecha, empleadoid,venta_total);
        Cven.modificarVenta(venta);
        Cven.cargarTablaVenta2(tblVenta);
        Cven.cargarTablaVenta1(table1);
        LimpiarCampos();




    }//GEN-LAST:event_btnModificarActionPerformed

