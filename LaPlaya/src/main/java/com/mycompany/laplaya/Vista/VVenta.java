      public void cargartablaVenta() {
    int filaselect = tblVenta.getSelectedRow();
    
    txtId.setText("" + tblVenta.getValueAt(filaselect, 0));
    

    
    cmbVCliente.setSelectedItem((String) tblVenta.getValueAt(filaselect, 1));
    cmbProducto.setSelectedItem((String) tblVenta.getValueAt(filaselect, 2));
    txtPrecio.setText(""+tblVenta.getValueAt(filaselect, 3));
    

    ////////////////////////////////////////CARGA EL SPINNER//////////////////////////////
    Object valorSpinner = tblVenta.getValueAt(filaselect, 4);
    if (valorSpinner instanceof Integer) {
        spCantidadPor.setValue((Integer) valorSpinner);
    } else {
        spCantidadPor.setValue(Integer.parseInt(valorSpinner.toString()));
    }

    ///////////////////////////////CArga la fecha//////////////////////////////
     Date fechaC=null;
        try {
            fechaC = form.parse(tblVenta.getValueAt(filaselect, 5).toString());
        } catch (ParseException ex) {
            Logger.getLogger(VEmpleado.class.getName()).log(Level.SEVERE, null, ex);
        }
        jdtFecha.setDate(fechaC);
    cmbVEmpleado.setSelectedItem((String) tblVenta.getValueAt(filaselect, 6));
    txtVentaTotal.setText("" + tblVenta.getValueAt(filaselect, 7));
    
}

     
      
      
      
      

      
      
